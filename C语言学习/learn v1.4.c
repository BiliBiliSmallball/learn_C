#include<stdio.h>
// 算数符：加减乘除
// 有两种版本（单目双目）
// 单目 +(正号) || -(负号)

// 加'减'乘'除'求余;     +,-,*,/,%;
// 求余只有整数,且只能是整数

// 注意:
// 对于整数间的除法(/),除不净将直接舍去小数部分
void NB(){
    double a = 1.666;
    double b = 2.799;
    printf("a为1.666,b为2.799\n");
    printf("a+b = %d,a-b = %d,a*b = %d,a/b = %d;\n",(a+b),(a-b),(a*b),(a/b));//一定要用数字型输出数字
	printf("a\%b = %d",(int)a % (int)b); 
}

int main(){
	NB();
	return 0;
}
