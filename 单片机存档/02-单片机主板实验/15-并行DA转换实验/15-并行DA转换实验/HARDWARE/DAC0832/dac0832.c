/***********************************************
函数名称：DAC_Init
功    能：DAC0832初始化
入口参数：无
返 回 值：无	          
************************************************/
void DAC_Init(void)
{
	P1M1 = 0x00;		//设置P1口为推挽输出
	P1M0 = 0xff;
	
	P0M1 = 0x00;		//设置P0^0 ~ P0^3 为推挽输出
	P0M0 = 0x0f;
	
	CS    = 0;  
	XFRE  = 0;
	WR_EN = 0;
	ILE   = 1;
}

