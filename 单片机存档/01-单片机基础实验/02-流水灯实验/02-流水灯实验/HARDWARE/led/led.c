#include "led.h"

/**************************************************
函数名称：Led_Configure
函数说明：LED初始化配置函数	
输入参数：	无			
返 回 值：	无			
**************************************************/
void Led_Configure(void)
{
	P4_Mode_OUT_PP(0x0f);	//设置P4端口为推挽输出模式
	LED0(0);				//初始化默认熄灭灯
	LED1(0);				//初始化默认熄灭灯
	LED2(0);				//初始化默认熄灭灯
	LED3(0);				//初始化默认熄灭灯
}

